package com.example.lab34.model;

public class TypeMong {
    String name;
    String dess;

    public TypeMong(String name, String dess) {
        this.name = name;
        this.dess = dess;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDess() {
        return dess;
    }

    public void setDess(String dess) {
        this.dess = dess;
    }
}
